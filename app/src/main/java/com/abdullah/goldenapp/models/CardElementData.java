package com.abdullah.goldenapp.models;

public class CardElementData {
    private String title;
    private double price;
    private String weight;
    private double changeRate;
    private double changePrice;
    private String k21;
    private String k18;
    private String k24;

    public CardElementData() {

    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public double getChangeRate() {
        return changeRate;
    }

    public void setChangeRate(double changeRate) {
        this.changeRate = changeRate;
    }

    public String getK21() {
        return k21;
    }

    public void setK21(String k21) {
        this.k21 = k21;
    }

    public String getK18() {
        return k18;
    }

    public void setK18(String k18) {
        this.k18 = k18;
    }

    public String getK24() {
        return k24;
    }

    public void setK24(String k24) {
        this.k24 = k24;
    }

    public double getChangePrice() {
        return changePrice;
    }

    public void setChangePrice(double changePrice) {
        this.changePrice = changePrice;
    }
}
